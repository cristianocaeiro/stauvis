package Entities;

import Database.FaecherDao;

public class Faecher {

  /*##############################################################################################################################################################*/
  /*##############################################################################################################################################################*/

  private Integer id;
  private String name;
  private static FaecherDao dao;

  /*##############################################################################################################################################################*/
  /*##############################################################################################################################################################*/

  /**
   * Um den DAO der Faecher zu erhalten.
   * @return den DAO der Faecher.
   */
  public static FaecherDao getDao() {

    if (dao == null) {
      dao = new FaecherDao();
    }
    return dao;
  }

  /*##############################################################################################################################################################*/
  /*##############################################################################################################################################################*/

  // GETTER und SETTERS
  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  @Override
  public String toString() {
    // TODO Auto-generated method stub
    return getName();
  }
}
